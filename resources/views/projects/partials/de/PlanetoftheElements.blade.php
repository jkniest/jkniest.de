<section>
    <p>
        Das Minispiel "Planet of the Elements" wurde für eine Game-Competition innerhalb von 50 Stunden, von zwei
        Leuten, entwickelt. Der Spieler hat dabei das Ziel in Wellen erscheinende Gegner zu töten, ohne selber zu
        sterben.
    </p>
    <br>
    <p>
        Dabei lassen diese Gegner Münzen fallen, die der Spieler in Shop-Automaten benutzen kann, um sich zufällige
        Items und Waffen zu kaufen. Insgesamt gibt es knapp 20 Items und fünf Waffen.
    </p>
</section>

<hr class="m3t">

<section class="m3t">
    <h3 class="title is-3">Funktionen</h3>

    <section class="m2t">

        <h4 class="title is-4">Zufällige Items</h4>

        <p>
            Es gibt knapp 20 Items, die alle miteinander kombinierbar und interagierbar sind. Manche der Items
            müssen manuell eingesetzt werden, während andere durchgehend einen Effekt haben, wie zum Beispiel
            Attribut-Erhöhungen (Geschwindigkeit, Schaden, ..) oder auch Zusatz-Effekte, wie Feuer-Schaden.
        </p>

    </section>

    <section class="m3t">

        <h4 class="title is-4">Gegner / Wellen</h4>

        <p>
            Insgesamt drei Gegner mit komplett unterschiedlichem Verhalten spawnen zufällig in Wellen in der Welt. Ein
            Gegner wird erst aktiviert, wenn der Spieler sich diesem nähert. Ein anderer widerrum macht Gift-Schaden.
            Jede Welle ist schwieriger als die vorherige, da zum einen die Gegner stärker werden, aber auch mehr Gegner
            erscheinen.
        </p>

    </section>

    <section class="m3t">

        <h4 class="title is-4">Shops / Kisten</h4>

        <p>
            Innerhalb des Levels befinden sich einige Shops und Kisten. Für eine gewisse Summe, kann man dort zufällige
            Items oder Waffen kaufen.
        </p>

    </section>

</section>

