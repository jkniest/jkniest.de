        48 Stunden veröffentlicht. Diesesmal war es: "Two button controls".
    </p>
    <br>
    <p>
        Die Welt, in der dieses Spiel spielt, wird von einem Kartoffel-Monster-König unterworfen. Der Spieler, ein
        Soldat, hat sich das Ziel gesetzt, den Kartoffel-Monster-König zu besiegen und wieder Frieden in die Welt
        zu bringen!
    </p>
