    <section class="m2t">

        <h4 class="title is-4">Steuerung</h4>

        <p>
            Der Spieler kann das komplette Spiel mit gerade mal zwei Tasten bestehen. Die Tasten "Q" und "E". Mit
            diesen Tasten kann der Spieler angreifen, ausweichen und sich fortbewegen.
        </p>

    </section>

    <section class="m3t">

        <h4 class="title is-4">Gegner</h4>

        <p>
            Auf dem Weg zum Kartoffel-Monster-Könfig begegnet man seinen Schergen, den normalen Kartoffel-Monstern.
            Diese müssen besiegt werden, bevor man zum König vorschreiten kann.
        </p>

    </section>

    <section class="m3t">

        <h4 class="title is-4">Boss-Kampf</h4>

        <p>
            Am Ende des Spiels muss ein Boss besiegt werden. Dieser schießt Kartoffeln auf euch, denen ihr ausweichen
            müsst.
        </p>

    </section>

</section>

