            Sämtliche Roboter und Waffen sind komplett animiert. Sie haben Schuss und Bewegungs-Animationen.
        </p>

    </section>

    <section class="m3t">

        <h4 class="title is-4">Soundeffekte / Musik</h4>

        <p>
            Das komplette Spiel hat zwei Soundtracks und alle Aktionen haben individuelle Sound-Effekte. Für die
            Erstellung kamen verschiedene Generatoren und Tools zum einsatz. Insgesamt erhöht dies die Stimmung in
            dem Spiel enorm und lässt dieses wesentlich realer wirken.
        </p>

    </section>

    <section class="m3t">

        <h4 class="title is-4">Energie</h4>

        <p>
            Der Spieler verliert stetig Energie. Auch wenn ein Gegner ihn verletzt wird ein Teil der eigenen Energie
            abgesaugt. Nach jedem geleertem Raum gibt es eine Batterie, die einen Teil der eigenen Energie auflädt.
        </p>
        <br>
        <p>
            Wenn keine Energie mehr vorhanden ist hat man das Spiel verloren.
        </p>

    </section>

