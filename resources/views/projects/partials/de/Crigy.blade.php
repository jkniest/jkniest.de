<section>
    <p>
        Das Minispiel "Crigy" wurde für die Ludum-Dare Competition entwickelt. Ziel, bei dem Ludum-Dare, ist es ein
        Videospiel innerhalb von 72 Stunden zu entwickeln. Am Anfang der Zeit wird ein Thema veröffentlicht, nach dem
        entwickelt werden sollte. Dieses mal kam das Thema: "Running out of Power".
    </p>
    <br>
    <p>
        Das Spiel spielt in einer verlassen Fabrik. Man schlüpft in die Rolle eines Roboters, dem die Energie ausgeht.
        Das Ziel des Spiels ist es, den ultimativen Generator zu aktivieren, um für sich selbst und für alle anderen
        Roboter unbegrenzt Energie erzeugen zu können.
    </p>
    <p>
        Aber Achtung: Andere Roboter wollen die ganze Energie für sich selber und wollen deine Energie. Sie werden
        dich angreifen und du musst dich verteidigen!
    </p>
</section>

