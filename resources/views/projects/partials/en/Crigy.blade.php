            much more real.
        </p>

    </section>

    <section class="m3t">

        <h4 class="title is-4">Energy</h4>

        <p>
            The player loses energy. If an enemy hurts him, a part of his own energy is sucked off. After each empty
            space, there is a battery that charges some of its own energy.
        </p>
        <br>
        <p>
            If there is no energy left, the game has been lost.
        </p>

