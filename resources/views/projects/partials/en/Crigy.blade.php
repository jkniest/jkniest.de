<section>
    <p>
        The video game "Crigy" was developed for the Ludum-Dare competition. Goal at the Ludum-Dare is
        to create a video game within 72 hours, according to a specific theme. The topic will be published at the start.
        This time it was: "Running out of Power".
    </p>
    <br>
    <p>
        The game plays in an abandoned factory. You slip into the role of a robot that runs out of energy. The goal of
        the game is to activate the ultimate generator in order to generate unlimited energy for yourself and all
        other robots.
    </p>
    <p>
        But beware: Other robots want the whole energy for themself and they want your energy. They will attack you
        and you must defend yourself!
    </p>
</section>

